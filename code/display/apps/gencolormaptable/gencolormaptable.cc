void parseColormapDefinition(Vector<Float> &reds, Vector<Float> &greens,
			     Vector<Float> &blues, Float *data, uInt size) {
  reds.resize(size);
  greens.resize(size);
  blues.resize(size);
  for (uInt j = 0; j < size; j++) {
    reds(j) = *data++;
    greens(j) = *data++;
    blues(j) = *data++;
  }
}

